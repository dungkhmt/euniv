package vn.webapp.modules.researchdeclarationmanagement.validation;

public class mBookSummaryValidation {

	private String bookReportingAcademicYear;
	
	private String bookFaculty;
	
	private String threadDepartment;
	
	private String threadStaff;

	public String getBookReportingAcademicYear() {
		return bookReportingAcademicYear;
	}

	public void setBookReportingAcademicYear(String bookReportingAcademicYear) {
		this.bookReportingAcademicYear = bookReportingAcademicYear;
	}

	public String getBookFaculty() {
		return bookFaculty;
	}

	public void setBookFaculty(String bookFaculty) {
		this.bookFaculty = bookFaculty;
	}
	
	public String getThreadDepartment() {
		return threadDepartment;
	}

	public void setThreadDepartment(String threadDepartment) {
		this.threadDepartment = threadDepartment;
	}

	public String getThreadStaff() {
		return threadStaff;
	}

	public void setThreadStaff(String threadStaff) {
		this.threadStaff = threadStaff;
	}

}
